    private void jButton_guardarProveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_guardarProveedorActionPerformed
        Proveedor proveedor = new Proveedor();
        Ctr_Proveedor controlProveedor = new Ctr_Proveedor();
        String estado = jComboBox_seleccione_estado.getSelectedItem().toString().trim();

        // Validar que no hayan campos vac√≠os
        if (txt_nombre.getText().equals("") || txt_direccion.getText().equals("") || txt_correo.getText().equals("") || txt_telefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        } else {
            // Validar existencia del proveedor
            if (!controlProveedor.existeProveedor(txt_nombre.getText().trim())) {
                if (estado.equalsIgnoreCase("Seleccione estado:")) {
                    JOptionPane.showMessageDialog(null, "Seleccione el estado");
                } else {
                    try {
                        // Asignar valores al proveedor
                        proveedor.setNombre(txt_nombre.getText().trim());
                        proveedor.setDireccion(txt_direccion.getText().trim());
                        proveedor.setTelefono(txt_telefono.getText().trim());
                        proveedor.setEmail(txt_correo.getText().trim());
                        proveedor.setDescripcion(txt_descripcion.getText().trim());
                        proveedor.setEstado(estado);

                        // Intentar guardar el proveedor
                        if (controlProveedor.guardarProveedores(proveedor)) {
                            JOptionPane.showMessageDialog(null, "Registro guardado");

                            // Limpiar campos solo despu√©s de un guardado exitoso
                            this.Limpiar();

                            // Restablecer el valor del ComboBox del estado
                            this.jComboBox_seleccione_estado.setSelectedItem("Seleccione estado:");
                        } else {
                            JOptionPane.showMessageDialog(null, "Error al guardar");
                        }

                    } catch (HeadlessException | NumberFormatException e) {
                        System.out.println("Error en: " + e);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "El proveedor ya existe");
            }
        }
    }//GEN-LAST:event_jButton_guardarProveedorActionPerformed

